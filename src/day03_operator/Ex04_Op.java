package day03_operator;

public class Ex04_Op {

	public static void main(String[] args) {

		/*
		 * true(참), false(거짓)
		  	a = 10, b = 20;
		  	a > b  : false
		  	a <= b  : true
		  	a == b  : false
		  	a != b  : true
		*/
		
		double do1 = 3.0, do2 = 2.1;
		System.out.println(do1 > do2); 		//true
		System.out.println(do1 <= do2); 	//false
		System.out.println(do1 == do2);		//flase
		System.out.println(do1 != do2); 	//true
		
	}

}
